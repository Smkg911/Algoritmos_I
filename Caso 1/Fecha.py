class Fecha:
    #aqui va el codigo

    '''------------------------
    # Atributos
    ---------------------------'''
    dia=0
    mes=0
    anio=0 

    '''----------------------------------
    # Metodos 
    -------------------------------------'''

    #constructor

    def __init__(self, dia=0, mes=0, anio=0):
        self.dia=dia
        self.mes=mes
        self.anio=anio

    def ConsultarDia(self):
        #aqui va el codigo
        return self.dia 
    
    def ConsultarMes(self):
        #aqui va el codigo 
        return self.mes
    
    def ConsultarAnio(self):
        #aqui va el codigo
        return self.anio 
